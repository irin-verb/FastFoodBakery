-- Создание таблицы "Клиент"
CREATE TABLE Клиент (
    КодКлиента INT PRIMARY KEY,
    Имя VARCHAR(15) NOT NULL,
    НомерТелефона CHAR(11) NOT NULL
        CHECK (
            НомерТелефона ~ '^[0-9]{11}$'
        )
);
-- Создание таблицы "Ресторан"
CREATE TABLE Ресторан (
    НомерРесторана INT PRIMARY KEY,
    Адрес VARCHAR(50) NOT NULL
);
-- Создание таблицы "Кассир"
CREATE TABLE Кассир (
    КодКассира INT PRIMARY KEY,
    Паспорт CHAR(10) NOT NULL
        CHECK (
            Паспорт ~ '^[0-9]{10}$'
        ),
    НомерРесторана INT,
    Фамилия VARCHAR(20) NOT NULL,
    Имя VARCHAR(15) NOT NULL,
    НомерТелефона CHAR(11) NOT NULL
        CHECK (
            НомерТелефона ~ '^[0-9]{11}$'
        ),
    ИНН CHAR(12) NOT NULL
        CHECK (
            ИНН ~ '^[0-9]{12}$'
        ),
    FOREIGN KEY (НомерРесторана)
        REFERENCES Ресторан(НомерРесторана)
);
-- Создание таблицы "КатегорияИзделия"
CREATE TABLE КатегорияИзделия (
    НомерКатегории INT PRIMARY KEY,
    Название VARCHAR(15) NOT NULL UNIQUE,
    Описание VARCHAR(50)
);
-- Создание таблицы "Изделие"
CREATE TABLE Изделие (
    КодИзделия INT PRIMARY KEY,
    НомерКатегории INT NOT NULL,
    Название VARCHAR(30) NOT NULL UNIQUE,
    Описание VARCHAR(200),
    ПищеваяЦенность INT NOT NULL,
    ТекущаяСтоимость FLOAT,
    FOREIGN KEY (НомерКатегории)
        REFERENCES КатегорияИзделия(НомерКатегории)
);
-- Создание таблицы "Ингредиент"
CREATE TABLE Ингредиент (
    КодИнгредиента INT PRIMARY KEY,
    Название VARCHAR(15) NOT NULL UNIQUE,
    Описание VARCHAR(50),
    Аллергенность BOOLEAN NOT NULL DEFAULT FALSE
);

-- Создание таблицы "СтатусЗаказа"
CREATE TABLE СтатусЗаказа (
    КодСтатуса INT PRIMARY KEY,
    Название VARCHAR(15) NOT NULL UNIQUE,
    Описание VARCHAR(50)
);
-- Создание таблицы "Заказ"
CREATE TABLE Заказ (
    НомерЗаказа INT PRIMARY KEY,
    КодКлиента INT NOT NULL,
    КодКассира INT NOT NULL,
    НомерРесторана INT NOT NULL,
    Стоимость FLOAT NOT NULL,
    НомерСтолика INT,
    НомерЛокатора INT,
    ВремяОформления TIMESTAMP NOT NULL,
    FOREIGN KEY (КодКлиента)
        REFERENCES Клиент(КодКлиента),
    FOREIGN KEY (КодКассира)
        REFERENCES Кассир(КодКассира),
    FOREIGN KEY (НомерРесторана)
        REFERENCES Ресторан(НомерРесторана)
);
-- Создание таблицы "СменаСтатусовЗаказа"
CREATE TABLE СменаСтатусовЗаказа (
    Код INT PRIMARY KEY,
    НомерЗаказа INT NOT NULL,
    ДатаВремя TIMESTAMP NOT NULL,
    КодСтатуса INT NOT NULL,
    FOREIGN KEY (НомерЗаказа)
        REFERENCES Заказ(НомерЗаказа),
    FOREIGN KEY (КодСтатуса)
        REFERENCES СтатусЗаказа(КодСтатуса)
);
-- Создание таблицы "МенюРесторана"
CREATE TABLE МенюРесторана (
    Код INT PRIMARY KEY,
    НомерРесторана INT NOT NULL,
    КодИзделия INT NOT NULL,
    FOREIGN KEY (НомерРесторана)
        REFERENCES Ресторан(НомерРесторана),
    FOREIGN KEY (КодИзделия)
        REFERENCES Изделие(КодИзделия)
);
-- Создание таблицы "СменаЦенНаИзделие"
CREATE TABLE СменаЦенНаИзделие (
    Код INT PRIMARY KEY,
    КодИзделия INT NOT NULL,
    ДатаВремя TIMESTAMP NOT NULL,
    Цена FLOAT NOT NULL,
    FOREIGN KEY (КодИзделия)
        REFERENCES Изделие(КодИзделия)
);
-- Создание таблицы "СоставИзделия"
CREATE TABLE СоставИзделия (
    Код INT PRIMARY KEY,
    КодИнгредиента INT NOT NULL,
    КодИзделия INT NOT NULL,
    FOREIGN KEY (КодИнгредиента)
        REFERENCES Ингредиент(КодИнгредиента),
    FOREIGN KEY (КодИзделия)
        REFERENCES Изделие(КодИзделия)
);
-- Создание таблицы "СоставЗаказа"
CREATE TABLE СоставЗаказа (
    Код INT PRIMARY KEY,
    НомерЗаказа INT NOT NULL,
    КодИзделия INT NOT NULL,
    Количество INT NOT NULL DEFAULT 1,
    FOREIGN KEY (НомерЗаказа)
        REFERENCES Заказ(НомерЗаказа),
    FOREIGN KEY (КодИзделия)
        REFERENCES Изделие(КодИзделия)
);

-- Дополнительные ограничения
-- Ограничение: пищевая ценность изделия больше нуля
ALTER TABLE Изделие
ADD CONSTRAINT chk_пищевая_ценность
CHECK (ПищеваяЦенность >= 0);
-- Ограничение: текущая стоимость больше нуля
ALTER TABLE Изделие
ADD CONSTRAINT chk_текущая_стоимость_положительная
CHECK (ТекущаяСтоимость > 0);
-- Ограничение: время оформления заказа не может быть в будущем
ALTER TABLE Заказ
ADD CONSTRAINT chk_время_оформления
CHECK (ВремяОформления <= CURRENT_TIMESTAMP);
-- Ограничение: если есть номер столика – он должен быть больше нуля
ALTER TABLE Заказ
ADD CONSTRAINT chk_столик_положительный
CHECK (НомерСтолика IS NULL OR НомерСтолика > 0);
-- Ограничение: если есть номер локатора – он должен быть больше нуля
ALTER TABLE Заказ
ADD CONSTRAINT chk_локатор_положительный
CHECK (НомерЛокатора IS NULL OR НомерЛокатора > 0);
-- Ограничение: стоимость заказа больше нуля
ALTER TABLE Заказ
ADD CONSTRAINT chk_стоимость_положительная
CHECK (Стоимость > 0);
-- Ограничение: назначение статуса не может быть в будущем
ALTER TABLE СменаСтатусовЗаказа
ADD CONSTRAINT chk_дата_время_не_в_будущем
CHECK (ДатаВремя <= CURRENT_TIMESTAMP);
-- Ограничение: смена цены не может быть в будущем
ALTER TABLE СменаЦенНаИзделие
ADD CONSTRAINT chk_дата_время_не_в_будущем
CHECK (ДатаВремя <= CURRENT_TIMESTAMP);
-- Ограничение: цена изделия положительна
ALTER TABLE СменаЦенНаИзделие
ADD CONSTRAINT chk_цена_положительная
CHECK (Цена > 0);
-- Ограничение: количество изделий добавляющихся в заказ положительно
ALTER TABLE СоставЗаказа
ADD CONSTRAINT chk_количество_положительное
CHECK (Количество > 0);


